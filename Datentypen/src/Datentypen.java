public class Datentypen {
    public static void main(String[] args) {

        // Primitiven Datentypen
        int ganzZahl = 6;
        System.out.println('I' + "nhalt Variable ganzZahl: " + ganzZahl);

        float gleitkommazahlFloat = 3.14f;
        double gleitkommazahlDouble = 3.14;

        boolean jaNein = true;
        System.out.println("Inhalt Variable jaNein: " + jaNein);

        char einzelnesZeichen = 'a';

        // Komplexe Datentypen
        String zeichenkette = "Ich bin ein String";
    }
}
